def flaten_to_image(input_tensor):
    original_shape_tensor = torch.tensor([]).to(input_tensor.device,input_tensor.dtype)
    for sample in input_tensor:
        flat_sample = torch.tensor([]).to(input_tensor.device,input_tensor.dtype)
        for row in sample:
            flat_sample = torch.cat((flat_sample, row))
        original_sample = torch.tensor([]).to(input_tensor.device,input_tensor.dtype)
        for i in range(64):
            row = flat_sample[i*64:(i+1)*64]
            original_sample = torch.cat((original_sample, row.unsqueeze(0)))  
        original_shape_tensor = torch.cat((original_shape_tensor, original_sample.unsqueeze(0)))  
    return original_shape_tensor

def image_to_flaten(input_tensor):
    reshaped_tensor = torch.tensor([]).to(input_tensor.device,input_tensor.dtype)
    for sample in input_tensor:
        flat_sample = torch.tensor([]).to(input_tensor.device,input_tensor.dtype)
        for row in sample:
            flat_sample = torch.cat((flat_sample, row))
        reshaped_sample = torch.tensor([]).to(input_tensor.device,input_tensor.dtype)
        for i in range(2):
            row = flat_sample[i*2048:(i+1)*2048]
            reshaped_sample = torch.cat((reshaped_sample, row.unsqueeze(0)))  
        reshaped_tensor = torch.cat((reshaped_tensor, reshaped_sample.unsqueeze(0)))  
    return reshaped_tensor

